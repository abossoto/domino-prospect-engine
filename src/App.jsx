function parseJSON(raw) {
  let str = raw.trim()
    .replace(/^```json\s*/i, "").replace(/^```\s*/i, "").replace(/\s*```$/i, "").trim();
  const s = str.indexOf("{");
  const e = str.lastIndexOf("}");
  if (s === -1 || e === -1) throw new Error("Risposta non in formato JSON");
  return JSON.parse(str.slice(s, e + 1));
}

