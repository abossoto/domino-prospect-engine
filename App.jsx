const sleep = (ms) => new Promise(r => setTimeout(r, ms));

// Chiama Anthropic con retry automatico su rate limit 429
async function callClaude({ apiKey, system, messages, tools, max_tokens = 5000 }) {
  const body = { model: "claude-sonnet-4-20250514", max_tokens, system, messages };
  if (tools) body.tools = tools;

  const MAX_RETRIES = 4;

  for (let attempt = 0; attempt <= MAX_RETRIES; attempt++) {
    const res = await fetch("https://api.anthropic.com/v1/messages", {
      method: "POST",
      headers: {
        "Content-Type": "application/json",
        "x-api-key": apiKey,
        "anthropic-version": "2023-06-01",
      },
      body: JSON.stringify(body),
    });

    if (res.status === 429) {
      if (attempt >= MAX_RETRIES) {
        throw new Error(
          "Rate limit raggiunto dopo diversi tentativi. " +
          "Per risolvere: vai su console.anthropic.com → Settings → Billing → Buy credits. " +
          "Con $40 totali di crediti acquistati sali al Tier 2 (5x piu capacita) automaticamente."
        );
      }
      const retryAfter = parseInt(res.headers.get("retry-after") || "20", 10);
      console.log("[Domino PE] Rate limit 429 — attendo " + retryAfter + "s (tentativo " + (attempt + 1) + "/" + MAX_RETRIES + ")");
      await sleep((retryAfter + 3) * 1000);
      continue;
    }

    if (!res.ok) {
      const err = await res.json().catch(() => ({}));
      throw new Error(err?.error?.message || "Anthropic HTTP " + res.status);
    }

    return res.json();
  }
}

